#include <iostream>
#include <vector>
#include <utility>
#include <algorithm>

int main() {
    std::ios::sync_with_stdio(false);
    std::cin.tie(nullptr);

    int w, n;
    std::cin >> w >> n;
    std::vector<std::pair<int, std::pair<int, int>>> dat(n + 1);
    for (int t = 1; t <= n; ++t)
        std::cin >> dat[t].first >> dat[t].second.first >> dat[t].second.second;
    std::sort(dat.begin(), dat.end(), [](const auto &a, const auto &b) {
        if (a.first != b.first)
            return a.first < b.first;
        return a.second.first < b.second.first;
    });

}